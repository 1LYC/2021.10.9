//1瓶汽水一块钱，2个空瓶可以换一瓶汽水，给20元可以喝多少瓶汽水？
#include<stdio.h>
int main()
{
	int money = 0;
	scanf("%d",&money);
	int he_shui = money;
	int empty = money;
	while (empty>1)
	{
		he_shui += (empty / 2);
		empty = empty / 2 + empty % 2;
	}
  	printf("%d", he_shui);
	//最后发现喝水数等于2*money-1     
	return 0;
}
